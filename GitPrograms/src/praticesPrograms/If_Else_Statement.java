/*
Java if-else Statement
The Java if-else statement also tests the condition. It executes the if block
 if condition is true otherwise else block is executed.

Syntax:

if(condition){  
//code if condition is true  
}else{  
//code if condition is false  
} 
*/
package praticesPrograms;

public class If_Else_Statement {

	public static void main(String[] args) {
		int i = 20;
		if (i == 10) // Condition
		{
			// if the given statment is correct if block will excute.
			System.out.println("It is Correct");
		} else {
			// else body
			System.out.println("It is not Correct");
		}

	}

}
